def get_insulation_data(name_list, voltage_int, spec_S_int):
    # 调用fn函数处理输入的名称列表，获取名称信息
    name = fn(name_list)
    d = []  # 初始化一个空列表，用于存储最终结果
    
    # 定义绝缘材料类型与数据源的映射字典
    # 键为绝缘材料类型名称，值为对应的数据源列表
    insulation_map = {
        "聚氯乙烯绝缘": d_pvc,
        "交联聚乙烯绝缘": d_XLPE,
        "EPR绝缘": d_EPR,
        "HEPR绝缘": d_HEPR
    }
    
    # 获取对应的数据源
    data_source = insulation_map.get(name[1])
    if not data_source:
        return d  # 如果没有匹配的绝缘材料类型，返回空列表
    
    # 根据电压等级选择列索引
    col_index = 1 if voltage_int[1] == 1 else 2
    
    # 提取数据
    for j in spec_S_int:
        for i in data_source:
        
            if j == i[0]:
                d.append(i[col_index])

    return sorted(d)
